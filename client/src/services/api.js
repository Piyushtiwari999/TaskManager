import axios from 'axios';

const api = axios.create({
  baseURL: import.meta.env.VITE_API_URL ? `${import.meta.env.VITE_API_URL}/api` : '/api',
  headers: {
    'Content-Type': 'application/json',
  },
});

// Add a request interceptor to include the token in headers
api.interceptors.request.use(
  (config) => {
    const token = localStorage.getItem('token');
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    return config;
  },
  (error) => Promise.reject(error)
);

// Add a response interceptor to handle errors globally
api.interceptors.response.use(
  (response) => response,
  (error) => {
    // Handle Network Errors (Server down, no internet)
    if (!error.response) {
      const networkError = new Error('Server is unreachable. Please check your connection or try again later.');
      networkError.isNetworkError = true;
      return Promise.reject(networkError);
    }

    if (error.response?.status === 401) {
      localStorage.removeItem('token');
      // Only redirect to login if we're not already there to avoid reload loops
      if (!window.location.pathname.includes('/login')) {
        window.location.href = '/login';
      }
    }
    return Promise.reject(error);
  }
);

export default api;
